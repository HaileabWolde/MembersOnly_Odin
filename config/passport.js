const passport = require("passport");
const pgPool = require("../db/pool")
const LocalStrategy = require('passport-local').Strategy;

const verifyCallback = async (username, password, done) => {
    try {
      const { rows } = await  pgPool.query("SELECT * FROM users WHERE username = $1", [username]);
      const user = rows[0];

      if (!user) {
        return done(null, false, { message: "Incorrect username" });
      }
      if (user.password !== password) {
        return done(null, false, { message: "Incorrect password" });
      }
      return done(null, user);
    } catch(err) {
      return done(err);
    }
  }
const startegy = new LocalStrategy(verifyCallback)
passport.use(startegy)
passport.serializeUser((user, done) => {
  done(null, user.id);
});

passport.deserializeUser(async (id, done) => {
  try {
    const { rows } = await  pgPool.query("SELECT * FROM users WHERE id = $1", [id]);
    const user = rows[0];

    done(null, user);
  } catch(err) {
    done(err);
  }
});
